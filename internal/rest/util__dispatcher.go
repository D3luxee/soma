package rest // import "github.com/mjolnir42/soma/internal/rest"

import (
	"fmt"
	"log"
	"net/http"
	"runtime/debug"
)

func panicCatcher(w http.ResponseWriter) {
	if r := recover(); r != nil {
		log.Printf("%s\n", debug.Stack())
		msg := fmt.Sprintf("PANIC! %s", r)
		http.Error(w, msg, http.StatusInternalServerError)
		return
	}
}

